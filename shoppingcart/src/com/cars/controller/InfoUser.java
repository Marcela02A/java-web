package com.cars.controller;

public class InfoUser {
	private String name;
	private String email;

	public InfoUser() {
	}

	public String get_name() {
		return name;
	}

	public void set_name(String _name) {
		this.name = _name;
	}

	public String get_email() {
		return email;
	}

	public void set_email(String _email) {
		this.email = _email;
	}

}